def get_random_cards():
    """Отримати випадковий набір карток для гравця"""
    import random

    profession = random.choice(PROFESSIONS)
    biology = random.choice(BIOLOGY)
    health = random.choice(HEALTH)
    hobby = random.choice(HOBBIES)
    baggage = random.choice(BAGGAGE)
    fact = random.choice(FACTS)
    special = random.choice(SPECIAL_CONDITIONS)

    return {
        "profession": profession,
        "biology": f"{biology['gender']}, {biology['age']} років",
        "health": health,
        "hobby": hobby,
        "baggage": baggage,
        "fact": fact,
        "special_condition": special,
    }


def get_random_catastrophe():
    """Отримати випадкову катастрофу"""
    import random

    catastrophe = random.choice(CATASTROPHES)
    side = random.choice(["A", "B"])

    if side == "A":
        return {
            "name": catastrophe["name"],
            "description": catastrophe["description"],
            "duration": catastrophe["duration"],
        }
    else:
        return {
            "name": catastrophe["name"] + " (Сторона B)",
            "description": catastrophe["side_b"],
            "duration": catastrophe.get("duration", "Невідомо"),
        }


def get_random_bunker_cards(count=5):
    """Отримати випадкові картки бункера"""
    import random

    return random.sample(BUNKER_CARDS, min(count, len(BUNKER_CARDS)))


